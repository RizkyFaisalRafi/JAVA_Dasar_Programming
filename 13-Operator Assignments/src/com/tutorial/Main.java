package com.tutorial;

public class Main {

    public static void main(String[] args) {

        //Operator Assignments (+)
        int a = 2;
        a += 10; // a = a + 2
        System.out.println("Hasil (a += 10) = " + a);

        //Operator Assignments (-)
        int b = 2;
        b -= 10; // b = b - 2
        System.out.println("Hasil (b -= 10) = " + b);

        //Operator Assignments (*)
        int c = 2;
        c *= 10; // c = c * 2
        System.out.println("Hasil (c *= 10) = " + c);

        //Operator Assignments (/)
        int d = 50;
        d /= 2; // d = d / 2
        System.out.println("Hasil (d /= 2) = " + d);

        //Operator Assignments (%)
        int e = 25;
        e %= 10; // e = e % 5
        System.out.println("Hasil (e %= 10) = " + e);

    }
}
